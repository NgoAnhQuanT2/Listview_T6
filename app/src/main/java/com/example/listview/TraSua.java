package com.example.listview;

public class TraSua
{
    private String TraSua;
    private int Hinh;

    public TraSua(String traSua, int hinh) {
        TraSua = traSua;
        Hinh = hinh;
    }

    public String getTraSua() {
        return TraSua;
    }

    public void setTraSua(String traSua) {
        TraSua = traSua;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }
}
